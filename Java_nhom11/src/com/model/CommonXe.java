package com.model;

/**
 *
 * @author DELL
 */
public class CommonXe {
    private String idxe;
    private String ten;
    private String hangxe;
    private String mausac;
    private int giaban;
    private int namsx;
    private int soluong;

    public CommonXe() {
    }

    public CommonXe(String idxe, String ten, String hangxe, String mausac, int giaban, int namsx, int soluong) {
        this.idxe = idxe;
        this.ten = ten;
        this.hangxe = hangxe;
        this.mausac = mausac;
        this.giaban = giaban;
        this.namsx = namsx;
        this.soluong = soluong;
    }

    public String getIdxe() {
        return idxe;
    }

    public void setIdxe(String idxe) {
        this.idxe = idxe;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getHangxe() {
        return hangxe;
    }

    public void setHangxe(String hangxe) {
        this.hangxe = hangxe;
    }

    public String getMausac() {
        return mausac;
    }

    public void setMausac(String mausac) {
        this.mausac = mausac;
    }

    public int getGiaban() {
        return giaban;
    }

    public void setGiaban(int giaban) {
        this.giaban = giaban;
    }

    public int getNamsx() {
        return namsx;
    }

    public void setNamsx(int namsx) {
        this.namsx = namsx;
    }

    public int getSoluong() {
        return soluong;
    }

    public void setSoluong(int soluong) {
        this.soluong = soluong;
    }

    
    
}
